  SUBROUTINE generate_bands_input(pw_bands_file, bandspp_file)
  !---------------------------------------------------------------------
    !
    ! Generate pw.x input for band structure calculation.
    ! Reads structural parameters from scf_input_file.
    ! Replaces calculation='scf' with calculation='bands'.
    ! Appends K_POINTS from bands_kpath_file.
    !
    USE elph_manager_input, ONLY : pw_input_file, prefix, outdir, &
                                   bands_kpath_file, verbose
    USE io_global,          ONLY : ionode, stdout
    !
    CHARACTER(LEN=256), INTENT(OUT) :: pw_bands_file, bandspp_file
    !
    INTEGER            :: iunit_in, iunit_out, iunit_kpath, ios
    CHARACTER(LEN=512) :: line, line_up
    LOGICAL            :: in_kpoints
    !
    pw_bands_file = 'pw_bands_auto.in'
    bandspp_file  = 'bandspp_auto.in'
    !
    IF (.NOT. ionode) RETURN
    !
    IF (LEN_TRIM(bands_kpath_file) == 0) &
       CALL errore('generate_bands_input', &
       'bands_kpath_file must be set when compute_bands = .true.', 1)
    !
    ! --- Generate pw.x bands input ---
    iunit_in  = 35
    iunit_out = 36
    !
    OPEN(UNIT=iunit_in, FILE=TRIM(pw_input_file), STATUS='OLD', &
         ACTION='READ', IOSTAT=ios)
    IF (ios /= 0) CALL errore('generate_bands_input', &
         'Cannot open pw_input_file: '//TRIM(pw_input_file), 1)
    !
    OPEN(UNIT=iunit_out, FILE=TRIM(pw_bands_file), &
         STATUS='REPLACE', ACTION='WRITE', IOSTAT=ios)
    IF (ios /= 0) CALL errore('generate_bands_input', &
         'Cannot create: '//TRIM(pw_bands_file), 1)
    !
    WRITE(iunit_out,'(A)') '! Auto-generated by elph_manager.x (bands)'
    !
    in_kpoints = .FALSE.
    DO
       READ(iunit_in, '(A)', IOSTAT=ios) line
       IF (ios /= 0) EXIT
       line_up = line
       CALL to_upper(line_up)
       !
       ! Replace calculation = 'scf' with 'bands'
       IF (is_keyword(line_up, 'CALCULATION')) THEN
          WRITE(iunit_out,'(A)') "  calculation = 'bands',"
          CYCLE
       END IF
       !
       ! Skip original K_POINTS card (we replace it with the path)
       IF (INDEX(TRIM(ADJUSTL(line_up)), 'K_POINTS') == 1) THEN
          in_kpoints = .TRUE.
          CYCLE
       END IF
       IF (in_kpoints) THEN
          ! Skip one line of K_POINTS data, then stop skipping
          in_kpoints = .FALSE.
          CYCLE
       END IF
       !
       WRITE(iunit_out,'(A)') TRIM(line)
    END DO
    CLOSE(iunit_in)
    !
    ! Append crystal_b k-path
    WRITE(iunit_out,'(A)') 'K_POINTS crystal_b'
    iunit_kpath = 37
    OPEN(UNIT=iunit_kpath, FILE=TRIM(bands_kpath_file), STATUS='OLD', &
         ACTION='READ', IOSTAT=ios)
    IF (ios /= 0) CALL errore('generate_bands_input', &
         'Cannot open bands_kpath_file: '//TRIM(bands_kpath_file), 1)
    DO
       READ(iunit_kpath, '(A)', IOSTAT=ios) line
       IF (ios /= 0) EXIT
       WRITE(iunit_out,'(A)') TRIM(line)
    END DO
    CLOSE(iunit_kpath)
    CLOSE(iunit_out)
    !
    ! --- Generate bands.x post-processing input ---
    iunit_out = 38
    OPEN(UNIT=iunit_out, FILE=TRIM(bandspp_file), &
         STATUS='REPLACE', ACTION='WRITE', IOSTAT=ios)
    IF (ios /= 0) CALL errore('generate_bands_input', &
         'Cannot create: '//TRIM(bandspp_file), 1)
    !
    WRITE(iunit_out,'(A)') '! Auto-generated by elph_manager.x (bands.x pp)'
    WRITE(iunit_out,'(A)') '&BANDS'
    WRITE(iunit_out,'(3A)') "  prefix  = '", TRIM(prefix), "',"
    WRITE(iunit_out,'(3A)') "  outdir  = '", TRIM(outdir), "',"
    WRITE(iunit_out,'(3A)') "  filband = '", TRIM(prefix), ".bands.dat',"
    WRITE(iunit_out,'(A)') '/'
    CLOSE(iunit_out)
    !
    IF (verbose) THEN
       WRITE(stdout,'(5X,A)') 'Generated bands input: '//TRIM(pw_bands_file)
       WRITE(stdout,'(5X,A)') 'Generated bandspp input: '//TRIM(bandspp_file)
    END IF
    !
  END SUBROUTINE generate_bands_input
