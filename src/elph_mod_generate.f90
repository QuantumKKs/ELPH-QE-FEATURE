  !---------------------------------------------------------------------
  SUBROUTINE generate_q2r_input(q2r_input_file)
  !---------------------------------------------------------------------
    !
    ! Generate q2r.x input to convert dyn matrices to force constants.
    ! q2r.x must be run before matdyn.x.
    !
    USE elph_manager_input, ONLY : ph_input_file, prefix, verbose
    USE io_global,          ONLY : ionode, stdout
    !
    CHARACTER(LEN=256), INTENT(OUT) :: q2r_input_file
    !
    INTEGER            :: iunit_ph, iunit_out, ios
    CHARACTER(LEN=512) :: line, line_up
    CHARACTER(LEN=256) :: fildyn_val
    INTEGER            :: nq1_val, nq2_val, nq3_val
    !
    q2r_input_file = 'q2r_auto.in'
    !
    IF (.NOT. ionode) RETURN
    !
    ! Parse fildyn and nq from ph_input_file
    fildyn_val = TRIM(prefix)//'.dyn'
    nq1_val = 2; nq2_val = 2; nq3_val = 2
    !
    iunit_ph = 33
    OPEN(UNIT=iunit_ph, FILE=TRIM(ph_input_file), STATUS='OLD', &
         ACTION='READ', IOSTAT=ios)
    IF (ios == 0) THEN
       DO
          READ(iunit_ph, '(A)', IOSTAT=ios) line
          IF (ios /= 0) EXIT
          line_up = line
          CALL to_upper(line_up)
          IF (INDEX(line_up, 'FILDYN') > 0 .AND. INDEX(line_up, '=') > 0) THEN
             ios = INDEX(line, "'")
             IF (ios > 0) THEN
                fildyn_val = line(ios+1:)
                ios = INDEX(fildyn_val, "'")
                IF (ios > 0) fildyn_val = fildyn_val(1:ios-1)
             END IF
          END IF
          IF (INDEX(line_up, 'NQ1') > 0 .AND. INDEX(line_up, '=') > 0) &
             READ(line(INDEX(line_up,'NQ1')+4:), *, IOSTAT=ios) nq1_val
          IF (INDEX(line_up, 'NQ2') > 0 .AND. INDEX(line_up, '=') > 0) &
             READ(line(INDEX(line_up,'NQ2')+4:), *, IOSTAT=ios) nq2_val
          IF (INDEX(line_up, 'NQ3') > 0 .AND. INDEX(line_up, '=') > 0) &
             READ(line(INDEX(line_up,'NQ3')+4:), *, IOSTAT=ios) nq3_val
       END DO
       CLOSE(iunit_ph)
    END IF
    !
    iunit_out = 34
    OPEN(UNIT=iunit_out, FILE=TRIM(q2r_input_file), &
         STATUS='REPLACE', ACTION='WRITE', IOSTAT=ios)
    IF (ios /= 0) CALL errore('generate_q2r_input', &
         'Cannot create: '//TRIM(q2r_input_file), 1)
    !
    WRITE(iunit_out,'(A)') '! Auto-generated by elph_manager.x'
    WRITE(iunit_out,'(A)') '&INPUT'
    WRITE(iunit_out,'(3A)') "  fildyn = '", TRIM(fildyn_val), "',"
    WRITE(iunit_out,'(3A)') "  flfrc  = '", TRIM(fildyn_val), ".fc',"
    WRITE(iunit_out,'(A)') "  zasr    = 'simple',"
    WRITE(iunit_out,'(A)') '/'
    CLOSE(iunit_out)
    !
    IF (verbose) WRITE(stdout,'(5X,A)') &
         'Generated q2r input: '//TRIM(q2r_input_file)
    !
  END SUBROUTINE generate_q2r_input
  !
  !---------------------------------------------------------------------
  SUBROUTINE generate_matdyn_input(matdyn_input_file)
  !---------------------------------------------------------------------
    !
    ! Generate matdyn.x input for phonon dispersion.
    ! Reads fildyn and q-grid from ph_input_file.
    ! Reads q-path from matdyn_qpath_file.
    !
    USE elph_manager_input, ONLY : ph_input_file, prefix, outdir, &
                                   matdyn_qpath_file, verbose
    USE io_global,          ONLY : ionode, stdout
    !
    CHARACTER(LEN=256), INTENT(OUT) :: matdyn_input_file
    !
    INTEGER            :: iunit_ph, iunit_out, iunit_path, ios
    CHARACTER(LEN=512) :: line, line_up
    CHARACTER(LEN=256) :: fildyn_val
    INTEGER            :: nq1_val, nq2_val, nq3_val
    LOGICAL            :: found_fildyn, found_nq
    !
    matdyn_input_file = 'matdyn_auto.in'
    !
    IF (.NOT. ionode) RETURN
    !
    IF (LEN_TRIM(matdyn_qpath_file) == 0) &
       CALL errore('generate_matdyn_input', &
       'matdyn_qpath_file must be set when compute_matdyn = .true.', 1)
    !
    ! --- Parse ph_input_file for fildyn and nq ---
    fildyn_val = TRIM(prefix)//'.dyn'
    nq1_val = 2; nq2_val = 2; nq3_val = 2
    found_fildyn = .FALSE.; found_nq = .FALSE.
    !
    iunit_ph = 30
    OPEN(UNIT=iunit_ph, FILE=TRIM(ph_input_file), STATUS='OLD', &
         ACTION='READ', IOSTAT=ios)
    IF (ios == 0) THEN
       DO
          READ(iunit_ph, '(A)', IOSTAT=ios) line
          IF (ios /= 0) EXIT
          line_up = line
          CALL to_upper(line_up)
          IF (INDEX(line_up, 'FILDYN') > 0 .AND. INDEX(line_up, '=') > 0) THEN
             ! Extract value between quotes
             ios = INDEX(line, "'")
             IF (ios > 0) THEN
                fildyn_val = line(ios+1:)
                ios = INDEX(fildyn_val, "'")
                IF (ios > 0) fildyn_val = fildyn_val(1:ios-1)
                found_fildyn = .TRUE.
             END IF
          END IF
          IF (INDEX(line_up, 'NQ1') > 0 .AND. INDEX(line_up, '=') > 0) THEN
             READ(line(INDEX(line_up,'NQ1')+4:), *, IOSTAT=ios) nq1_val
             found_nq = .TRUE.
          END IF
          IF (INDEX(line_up, 'NQ2') > 0 .AND. INDEX(line_up, '=') > 0) THEN
             READ(line(INDEX(line_up,'NQ2')+4:), *, IOSTAT=ios) nq2_val
          END IF
          IF (INDEX(line_up, 'NQ3') > 0 .AND. INDEX(line_up, '=') > 0) THEN
             READ(line(INDEX(line_up,'NQ3')+4:), *, IOSTAT=ios) nq3_val
          END IF
       END DO
       CLOSE(iunit_ph)
    END IF
    !
    ! --- Write matdyn.x input ---
    iunit_out = 31
    OPEN(UNIT=iunit_out, FILE=TRIM(matdyn_input_file), &
         STATUS='REPLACE', ACTION='WRITE', IOSTAT=ios)
    IF (ios /= 0) CALL errore('generate_matdyn_input', &
         'Cannot create: '//TRIM(matdyn_input_file), 1)
    !
    WRITE(iunit_out,'(A)') '! Auto-generated by elph_manager.x'
    WRITE(iunit_out,'(A)') '&INPUT'
    WRITE(iunit_out,'(3A)') "  flfrc   = '", TRIM(fildyn_val), ".fc',"
    WRITE(iunit_out,'(A)') "  flfrq   = 'matdyn.freq',"
    WRITE(iunit_out,'(A)') "  flvec   = 'matdyn.modes',"
    WRITE(iunit_out,'(A)') "  dos     = .false.,"
    WRITE(iunit_out,'(A)') "  asr              = 'simple',"
    WRITE(iunit_out,'(A)') "  q_in_band_form   = .true.,"
    WRITE(iunit_out,'(A)') '/'
    !
    ! Append q-path from matdyn_qpath_file
    iunit_path = 32
    OPEN(UNIT=iunit_path, FILE=TRIM(matdyn_qpath_file), STATUS='OLD', &
         ACTION='READ', IOSTAT=ios)
    IF (ios /= 0) CALL errore('generate_matdyn_input', &
         'Cannot open matdyn_qpath_file: '//TRIM(matdyn_qpath_file), 1)
    DO
       READ(iunit_path, '(A)', IOSTAT=ios) line
       IF (ios /= 0) EXIT
       WRITE(iunit_out,'(A)') TRIM(line)
    END DO
    CLOSE(iunit_path)
    CLOSE(iunit_out)
    !
    IF (verbose) WRITE(stdout,'(5X,A)') &
         'Generated matdyn input: '//TRIM(matdyn_input_file)
    !
  END SUBROUTINE generate_matdyn_input
